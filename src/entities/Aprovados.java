package entities;

public class Aprovados {
	
	private String name;
	private double n1;
	private double n2;
	
	public Aprovados (String name, double n1, double n2) {
		this.name = name;
		this.n1 = n1;
		this.n2 = n2;
	}

	public String getName() {
		return name;
	}
	
	public double getN1() {
		return n1;
	}
	
	public double getN2() {
		return n2;
	}
	

	}
